import numpy as np
import os, sys, glob
sys.path.insert(0, os.path.dirname(os.path.dirname(os.path.abspath(__file__))))

from uaf.core import integrate_network
import scipy.sparse as sp
import scipy.sparse.linalg as sla


AL_S = 0.06
F = 0.002
DELTA = 0.012
K0 = (DELTA - F) / AL_S
FWDIR = 'data/real/mjs_foodwebs'


def load_symmetrized(path):
    """Edge-list (predator<-prey) → symmetrized adjacency (mutual interaction)."""
    E = []
    for line in open(path):
        p = line.split()
        if len(p) >= 2:
            try:
                a, b = int(p[0]), int(p[1]); E.append((a, b))
            except ValueError:
                pass
    nodes = sorted(set([x for e in E for x in e]))
    idx = {n: i for i, n in enumerate(nodes)}
    N = len(nodes)
    A = np.zeros((N, N))
    for a, b in E:
        A[idx[a], idx[b]] = 1
        A[idx[b], idx[a]] = 1   # symmetrize: interaction is mutual
    np.fill_diagonal(A, 0)       # remove cannibalism self-loops
    return A


def lmax(A):
    return float(sla.eigsh(sp.csr_matrix(A.astype(float)), k=1, which='LA',
                           return_eigenvectors=False)[0])


def A0_crit(adj, T=800, dt=0.6, n_bisect=17):
    n = len(adj); lo, hi = 0.001, 0.95
    for _ in range(n_bisect):
        mid = (lo + hi) / 2
        tr = integrate_network(np.full(n, mid), adj, T=T, dt=dt, delta=DELTA)
        if tr[-1].mean() > 0.5:
            hi = mid
        else:
            lo = mid
    return (lo + hi) / 2


def giant_component(A):
    n = len(A); seen = np.zeros(n, bool); best = []
    for s in range(n):
        if seen[s]:
            continue
        st = [s]; c = []
        while st:
            u = st.pop()
            if seen[u]:
                continue
            seen[u] = True; c.append(u); st.extend(np.where(A[u] > 0)[0])
        if len(c) > len(best):
            best = c
    b = np.array(best)
    return A[b][:, b]


# ── EXP 043-A: directed artifact (why v1 said "boundary") ────────────────────
def exp_043_a():
    print("\n" + "="*72)
    print("EXP 043-A  Почему направленная матрица даёт ложную 'границу'")
    print("="*72)
    files = sorted(glob.glob(f'{FWDIR}/*.dat'))
    p = [x for x in files if 'chesapeake' in x][0]
    # directed load
    E = []
    for line in open(p):
        q = line.split()
        if len(q) >= 2:
            try:
                a, b = int(q[0]), int(q[1]); E.append((a, b))
            except ValueError:
                pass
    nodes = sorted(set([x for e in E for x in e]))
    idx = {n: i for i, n in enumerate(nodes)}
    N = len(nodes); Adir = np.zeros((N, N))
    for a, b in E:
        Adir[idx[a], idx[b]] = 1
    np.fill_diagonal(Adir, 0)
    basal = (Adir.sum(0) == 0).sum()
    print(f"""
  chesapeake: N={N}, базальных видов (k_in=0) = {basal}
  В направленной матрице базальные виды не получают входа в ABM-член
  α_s·Σ C_ij·A_j → коллапсируют при любом A0 → водораздела НЕТ.
  Отсюда ложный вердикт "граница −42%" в первой версии Q43.

  Решение: симметризация (взаимодействие обоюдно). См. 043-B.
""")


# ── EXP 043-B: law holds on 42 symmetrized food webs ─────────────────────────
def exp_043_b():
    print("\n" + "="*72)
    print("EXP 043-B  Закон на 42 симметризованных пищевых сетях")
    print("="*72)
    files = sorted(glob.glob(f'{FWDIR}/*.dat'))
    print(f"\n  {'сеть':>15}  {'N':>4}  {'λmax':>6}  {'A0':>7}  {'ошибка':>7}")
    print("  " + "-"*46)
    errs = []; sizes = []
    show = {'benguela', 'skipwith', 'reef', 'little_rock', 'chesapeake',
            'st_marks', 'cayman_islands', 'Ythan96', 'el_verde', 'Weddel_sea'}
    for p in files:
        name = os.path.basename(p).replace('.dat', '').replace('txt', '')
        A = giant_component(load_symmetrized(p))
        if len(A) < 15:
            continue
        a0 = A0_crit(A)
        if a0 > 0.9:
            continue
        lm = lmax(A); e = (a0 - K0/lm) / (K0/lm) * 100
        errs.append(e); sizes.append(len(A))
        if name in show:
            print(f"  {name:>15}  {len(A):>4}  {lm:>6.2f}  {a0:>7.4f}  {e:>+6.1f}%")
    errs = np.array(errs)
    print(f"""
  ... ({len(errs)} сетей всего, показаны классические)

  Средняя |ошибка| = {np.mean(np.abs(errs)):.1f}%
  Медиана |ошибки| = {np.median(np.abs(errs)):.1f}%
  Все ошибки отрицательные: {(errs<0).all()} (систематическое смещение)
  Диапазон: [{errs.min():.1f}%, {errs.max():.1f}%], N={min(sizes)}–{max(sizes)}
""")
    return errs


